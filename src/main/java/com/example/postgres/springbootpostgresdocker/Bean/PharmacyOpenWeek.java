package com.example.postgres.springbootpostgresdocker.Bean;

public class PharmacyOpenWeek {

	private String pharmacyName;
	private String openWeek;
	
	
	public PharmacyOpenWeek() {
		
	}
	
	public String getPharmacyName() {
		return pharmacyName;
	}
	public void setPharmacyName(String pharmacyName) {
		this.pharmacyName = pharmacyName;
	}
	public String getOpenWeek() {
		return openWeek;
	}
	public void setOpenWeek(String openWeek) {
		this.openWeek = openWeek;
	}
	
	
	
}
